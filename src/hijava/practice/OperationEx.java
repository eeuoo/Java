public class OperationEx {

	public static void main(String[] args) {
		int fatherAge = 45;
		int motherAge = 47;
		
		char ch;
		
		ch = (fatherAge > motherAge) ? 'T' : 'F' ;
		
		char sh;
		
		sh = (fatherAge < motherAge) ? 'T' : 'F' ;
		
		System.out.println(ch);
		System.out.println(sh);

	}

}
